/* ========== 底层像素操作 ========== */
static inline void _fb_pixel(uint32_t *fb, int fb_w, int x, int y, uint32_t c) {
    if(x<0||x>=fb_w||y<0||y>=600) return;
    fb[y*fb_w+x]=c;
}

static inline void _fb_fill(uint32_t *fb, int fb_w, int x,int y,int w,int h, uint32_t c) {
    for(int j=0;j<h;j++) {
        uint32_t *row=&fb[(y+j)*fb_w+x];
        for(int i=0;i<w;i++) row[i]=c;
    }
}

static inline void _fb_hline(uint32_t *fb, int fb_w, int x,int y,int w,uint32_t c) {
    if(y<0||y>=600) return;
    uint32_t *row=&fb[y*fb_w];
    for(int i=0;i<w;i++) { int px=x+i; if(px>=0&&px<fb_w) row[px]=c; }
}

/* ========== 文本渲染 ========== */
static void ui_draw_char(uint32_t *fb, int fb_w, int ch, int x, int y, uint32_t color) {
    if(ch<32||ch>122) ch='?';
    const unsigned char *glyph=_font[ch-32];
    for(int row=0;row<FONT_H;row++){
        unsigned char bits=glyph[row];
        for(int col=0;col<FONT_W;col++){
            if(bits&(0x80>>col))
                _fb_pixel(fb,fb_w,x+col,y+row,color);
        }
    }
}

static void ui_draw_text(uint32_t *fb,int fb_w,const char *s,int x,int y,uint32_t color){
    while(*s){
        if(*s=='\n'){x=0;y+=FONT_H+2;s++;continue;}
        ui_draw_char(fb,fb_w,*s,x,y,color);
        x+=FONT_W;
        s++;
    }
}

static void ui_draw_text_center(uint32_t *fb,int fb_w,const char *s,int cx,int y,uint32_t color){
    int w=strlen(s)*FONT_W;
    ui_draw_text(fb,fb_w,s,cx-w/2,y,color);
}

/* ========== 按钮渲染 ========== */
static void ui_draw_button(uint32_t *fb,int fb_w,int x,int y,int w,int h,
                           const char *label,int active,uint32_t color){
    uint32_t bg=active?color:COLOR_BTN_BG;
    _fb_fill(fb,fb_w,x+1,y+1,w-2,h-2,bg);
    /* 1px边框 */
    _fb_hline(fb,fb_w,x,y,w,COLOR_BORDER);
    _fb_hline(fb,fb_w,x,y+h-1,w,COLOR_BORDER);
    for(int i=0;i<h;i++){ _fb_pixel(fb,fb_w,x,y+i,COLOR_BORDER); _fb_pixel(fb,fb_w,x+w-1,y+i,COLOR_BORDER); }
    ui_draw_text_center(fb,fb_w,label,x+w/2,y+(h-FONT_H)/2,active?COLOR_WHITE:COLOR_TEXT);
}

/* ========== 日志API ========== */
static void ui_log_add(const char *fmt, ...) {
    va_list ap;
    va_start(ap,fmt);
    int idx=g_ui.log_head;
    vsnprintf(g_ui.logs[idx].text,LOG_LEN,fmt,ap);
    g_ui.logs[idx].ts=time(NULL);
    g_ui.log_head=(idx+1)%LOG_MAX;
    if(g_ui.log_count<LOG_MAX) g_ui.log_count++;
    va_end(ap);
}

/* ========== 主渲染函数 ========== */
static void ui_init(void) {
    memset(&g_ui,0,sizeof(g_ui));
    g_ui.camera_on=1;
    g_ui.btn_x=920; g_ui.btn_y=3; g_ui.btn_w=96; g_ui.btn_h=24;
}

/*
 * 渲染整帧UI布局:
 *   [ Top bar: status + FPS + toggle button ]
 *   [ Camera feed area (caller draws camera separately)  ]
 *   [ Defect log panel ]
 *
 * 调用顺序:
 *   1. ui_render_top_bar(fb,fb_w)      — 先画状态栏
 *   2. 调用者画摄像头画面到 (0,UI_CAM_Y) 区域
 *   3. ui_render_log_panel(fb,fb_w)     — 再画日志面板
 */
static void ui_render_top_bar(uint32_t *fb, int fb_w) {
    /* 背景 */
    _fb_fill(fb,fb_w,0,0,fb_w,UI_TOP_H,COLOR_PANEL);
    _fb_hline(fb,fb_w,0,UI_TOP_H-1,fb_w,COLOR_BORDER);

    /* 左侧: 系统标题 */
    char buf[64];
    int tx=8, ty=7;
    ui_draw_text(fb,fb_w,"SOLAR",tx,ty,COLOR_ACCENT); tx+=5*FONT_W;
    ui_draw_text(fb,fb_w,"|",tx,ty,COLOR_TEXT_DIM); tx+=FONT_W;
    ui_draw_text(fb,fb_w,"Defect Detection",tx,ty,COLOR_TEXT); tx+=17*FONT_W;

    /* 中间: FPS */
    tx=fb_w/2-40;
    snprintf(buf,sizeof(buf),"FPS:%d",g_ui.fps);
    ui_draw_text(fb,fb_w,buf,tx,ty,COLOR_GREEN);

    /* 摄像头状态指示 */
    tx=fb_w/2+50;
    snprintf(buf,sizeof(buf),"%d det",g_ui.detection_count);
    ui_draw_text(fb,fb_w,buf,tx,ty,COLOR_YELLOW);

    /* 右侧: 开关按钮 */
    snprintf(buf,sizeof(buf),"%s",g_ui.camera_on?"[ ON ]":"[OFF]");
    ui_draw_button(fb,fb_w,g_ui.btn_x,g_ui.btn_y,g_ui.btn_w,g_ui.btn_h,
                   buf,g_ui.camera_on,g_ui.camera_on?COLOR_BTN_ON:COLOR_BTN_OFF);

    /* 按钮提示 */
    tx=g_ui.btn_x-120; ty=10;
    ui_draw_text(fb,fb_w,"S:toggle",tx,ty,COLOR_TEXT_DIM);
}

static void ui_render_camera_off(uint32_t *fb, int fb_w) {
    /* 摄像头关闭时的占位画面 */
    _fb_fill(fb,fb_w,0,UI_CAM_Y,fb_w,UI_CAM_H,COLOR_CAM_OFF);

    /* 居中提示文字 */
    const char *lines[]={
        "Camera Off",
        "",
        "Press 'S' to start",
        "Serial: COM7 | Model: YOLOv8n | Platform: RV1126B",
        NULL
    };
    int total_h=0, n=0;
    for(int i=0;lines[i];i++){ total_h+=(FONT_H+4); n++; }
    int start_y=UI_CAM_Y+UI_CAM_H/2-total_h/2;

    for(int i=0;i<n;i++){
        uint32_t c=(i==0)?COLOR_ACCENT:COLOR_TEXT_DIM;
        ui_draw_text_center(fb,fb_w,lines[i],fb_w/2,start_y+i*(FONT_H+4),c);
    }
}

static void ui_render_log_panel(uint32_t *fb, int fb_w) {
    int lx=0, ly=UI_LOG_Y, lw=fb_w, lh=UI_LOG_H;

    /* 面板背景 */
    _fb_fill(fb,fb_w,lx,ly,lw,lh,COLOR_PANEL);
    _fb_hline(fb,fb_w,lx,ly,lw,COLOR_BORDER);
    _fb_hline(fb,fb_w,lx,ly+lh-1,lw,COLOR_BORDER);

    /* 标题 */
    char buf[128];
    snprintf(buf,sizeof(buf),"DEFECT  LOG  [%d entries]",g_ui.log_count);
    ui_draw_text(fb,fb_w,buf,8,ly+4,COLOR_ACCENT);

    /* 时间 */
    time_t now=time(NULL);
    struct tm *tm=localtime(&now);
    snprintf(buf,sizeof(buf),"%02d:%02d:%02d",tm->tm_hour,tm->tm_min,tm->tm_sec);
    ui_draw_text(fb,fb_w,buf,fb_w-80,ly+4,COLOR_TEXT_DIM);

    /* 日志条目 (最近N条，从新到旧) */
    int vis_rows=(lh-24)/18;  /* 每行18px高 */
    int row_y=ly+24;
    int total=g_ui.log_count;
    for(int i=0;i<vis_rows&&i<total;i++){
        int idx=(g_ui.log_head-i-1+LOG_MAX)%LOG_MAX;
        ui_log_t *l=&g_ui.logs[idx];
        int ry=row_y+i*18;

        /* 时间戳 */
        struct tm *lt=localtime(&l->ts);
        char ts[16];
        snprintf(ts,sizeof(ts),"%02d:%02d:%02d",lt->tm_hour,lt->tm_min,lt->tm_sec);
        ui_draw_text(fb,fb_w,ts,8,ry,COLOR_TEXT_DIM);

        /* 分隔线 */
        _fb_pixel(fb,fb_w,68,ry+8,COLOR_BORDER);

        /* 日志内容: 根据类别着色 */
        uint32_t tc=COLOR_TEXT;
        if(strstr(l->text,"bird-drop"))       tc=COLOR_RED;
        else if(strstr(l->text,"dusty"))      tc=COLOR_YELLOW;
        else if(strstr(l->text,"clean"))      tc=COLOR_GREEN;
        else if(strstr(l->text,"electrical")) tc=0x00FF44FF; /* 紫色 */
        else if(strstr(l->text,"physical"))   tc=0x0044FFFF; /* 青色 */
        else if(strstr(l->text,"snow"))       tc=COLOR_ORANGE;
        else if(strstr(l->text,"WARN"))       tc=COLOR_YELLOW;
        else if(strstr(l->text,"ERR"))        tc=COLOR_RED;

        /* 截断过长的文本 */
        char disp[96];
        snprintf(disp,sizeof(disp),"%.90s",l->text);
        ui_draw_text(fb,fb_w,disp,76,ry,tc);
    }
}

/*
 * 单次渲染全部UI (不含摄像头画面)
 * 调用者需先清除/绘制摄像头区域，再调用此函数
 */
static void ui_render_overlay(uint32_t *fb, int fb_w) {
    if(g_ui.camera_on)
        ui_render_top_bar(fb, fb_w);
    else {
        ui_render_top_bar(fb, fb_w);
        ui_render_camera_off(fb, fb_w);
    }
    ui_render_log_panel(fb, fb_w);
}

